"use-strict";

/**
 * Description
 * @param {number} x
 * @param {number} y
 * @exports add @returns {number} - Returns sum of x and y
 */
export const add = (x, y) => {
	return x + y;
};
/**
 * Description
 * @param {number} x
 * @param {number} y
 * @exports subtract @returns {number}
 */
export const subtract = (x, y) => {
	return x - y;
};

/**
 * Description
 * @param {number} x
 * @param {number} y
 * @exports multiply @returns {number}
 */
export const multiply = (x, y) => {
	return x * y;
};

/**
 * Description
 * @param {number} x
 * @param {number} y
 * @exports divide @returns {number} - Quotient of x and y
 */
export const divide = (x, y) => {
	return x / y;
};
